# Flask 

# It allow us to make webs Application fast and easy
# It is micro web framework in Python
# It is extensible (Add only what you need) Flask doesn’t force you to use lots of built-in features you might not need. Instead, you can add only the functionality your app requires.
# It is Lightweight (low memory only important dependencies) and flexible 
# Flask is based on werkzeug for routing and jinja 2 for templating 
# Flask = the app framework you use
# Werkzeug = handles “web requests and routing”
# Jinja2 = handles “dynamic HTML pages”
# WSGI complaint  (Web server gateaway interface) A standard interface between web servers and Python web apps (like Flask or Django).


